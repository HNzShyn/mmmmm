//Khai báo biến bằng "var"
var a = 3
var b = 5
var c = a + b
var d = a * b
console.log("a = " + a)
console.log("b = " + b)
console.log("c = " + c)
console.log("d = " + d)

//2. Khai báo biến bằng "let"
let city = "HaNoi"
let country = "VietNam"
console.log(city + " is the capital of " + country)

//3. Khai báo biến bằng "const"
const pi = 3.14
console.log("Pi = " + pi) 


